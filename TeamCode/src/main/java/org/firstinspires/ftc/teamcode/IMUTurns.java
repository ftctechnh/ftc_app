package org.firstinspires.ftc.teamcode;

import com.qualcomm.robotcore.eventloop.opmode.Autonomous;
import com.qualcomm.robotcore.eventloop.opmode.LinearOpMode;

/**
 * Created by JeremyYao on 12/27/2017.
 */
@Autonomous(name = "IMU TURNS TEST0", group = "Sensors")
public class IMUTurns extends LinearOpMode
{
    NewRobotFinal robot;
    public void runOpMode()
    {
        robot = new NewRobotFinal(hardwareMap);
        robot.initVuforia(hardwareMap);
        waitForStart();
        robot.driveStraight_In(10);
        robot.pivot_IMU(90, .5);
        robot.pivot_IMU(-90, .5);
        robot.pivot_IMU(90, .5);
        robot.pivot_IMU(-90, .5);
        sleep(4000);
        robot.stopAllMotors();
        robot.kill();
        robot.killAuto();
    }
}